package basic.controlflowstatement.decision_statement.if_else;

class DivisibleBy7 {

    public static void main(String[] args) {
        int a = 14;
        if (a % 7 == 0) {
            System.out.println("Division by 7 is possible");
        } else {
            System.out.println("divisible is not possible by 7");
        }
    }
}