package basic.operators.relation;

class Realtion {
    public static void main(String[] args) {
        int a = 10;
        int b = 12;
        System.out.print("a==10: ");
        System.out.println(a == 10);
        System.out.print("a<b: ");
        System.out.println(a < b);
        System.out.print("a>b: ");
        System.out.println(a > b);
        System.out.print("a<=b: ");
        System.out.println(a <= b);
        System.out.print("a>=b: ");
        System.out.println(a >= b);
        System.out.print("a!=10: ");
        System.out.println(a != 10);

    }
}