package basic.operators.assignment;

class Assingment {
    public static void main(String[] args) {
//        =,+=,-=,*=,/=,%=

        int a = 10; //Assigning value 10 to variable b using = operator
        int b = 20; //Assigning value 20 to variable b using = operator

        b += a;     // It is same as b = b+a;
        b -= a;     // It is same as b = b-a;
        b *= a;     // It is same as b = b*a;
        b /= a;     // It is same as b = b/a;
        b %= a;     // It is same as b = b%a;


    }
}